require 'uri'
require 'net/https'
require 'json'

class JpWiki
  attr_reader :city, :title

  def initialize(city)
    @city = city
    @title = get_link.split('/').last
  end

  def get_link
    link = index[city.to_sym]
    return "https://en.wikipedia.org/wiki/#{city}" if link.nil?

    link
  end

  def get_description
    json['extract']
  end

  def get_title
    json['title']
  end

  private

  def json
    query = "https://ja.wikipedia.org/w/api.php?format=json&action=query&prop=extracts&exintro&explaintext&titles=#{jp_name}"
    uri = URI.parse(query)
    json = Net::HTTP.get(uri)
    JSON.parse(json)['query']['pages'].to_a.flatten.last
  end

